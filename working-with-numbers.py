# from math import * will allow us to do more with math
from math import *
print(2)
print(2 * (2+3))
print(10 % 3)  # This is read 10 mod 3
my_num = 5
print(str(my_num) + " is a cool number.")
# str() converts number to string
print(pow(2, 4))
# pow(number, exponent for number)
my_num = -5
print(abs(my_num))  # abs() converts to absolute
print(my_num)
print(max(1, 9))
# max() calculates which number is bigger
new_num = 7
print(max(my_num, new_num))
# min() calculates which number is smaller
print(round(3.5))
# round() will round to nearest whole number whether smaller or larger than decimal
print(floor(3.7))
# floor() will essentially print number without decimal
print(ceil(5.1))
# ceil() will round up number no matter what just as long as it has a decimal
